new_val   = ADDITIONS[split:]

train_rows.extend(new_train)
val_rows.extend(new_val)

random.shuffle(train_rows)
random.shuffle(val_rows)

save(TRAIN_IN, train_rows)
save(VAL_IN,   val_rows)

print(f"Total examples : {len(train_rows)+len(val_rows)}")
print(f"Training rows  : {len(train_rows)}")
print(f"Validation rows: {len(val_rows)}")
print(f"New examples added: {len(ADDITIONS)} (+{split} train, +{len(new_val)} val)")

# class counts
from collections import Counter
counts = Counter(r["label"] for r in train_rows)
print(f"\nSmallest 10:")
for k,v in sorted(counts.items(), key=lambda x: x[1])[:10]:
    print(f"  {k:<45} {v}")
print(f"\nModified classes:")
modified = set(r["label"] for r in ADDITIONS)
for k in sorted(modified):
    print(f"  {k:<45} {counts[k]}")
